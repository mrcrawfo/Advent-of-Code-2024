const test1 = (input) => {
	let total = 0;

	const testFromX = (tx, ty, dx, dy) => {
		try {
			if (
				input[ty + dy].charAt(tx + dx) === "M" &&
				input[ty + 2 * dy].charAt(tx + 2 * dx) === "A" &&
				input[ty + 3 * dy].charAt(tx + 3 * dx) === "S"
			) {
				return 1;
			}
		} catch (e) {
			return 0;
		}

		return 0;
	};

	for (let y = 0; y < input.length; y++) {
		const row = input[y];
		for (let x = 0; x < row.length; x++) {
			const c = row.charAt(x);
			if (c === "X") {
				// up
				total += testFromX(x, y, 0, -1);
				// up-right
				total += testFromX(x, y, 1, -1);
				// right
				total += testFromX(x, y, 1, 0);
				// down-right
				total += testFromX(x, y, 1, 1);
				// down
				total += testFromX(x, y, 0, 1);
				// down-left
				total += testFromX(x, y, -1, 1);
				// left
				total += testFromX(x, y, -1, 0);
				// up-left
				total += testFromX(x, y, -1, -1);
			}
		}
	}

	return total;
};

const test2 = (input) => {
	let total = 0;

	const testFromA = (tx, ty) => {
		if (
			tx > 0 &&
			tx < input[ty].length - 1 &&
			ty > 0 &&
			ty < input.length - 1
		) {
			// up-right
			const da1 = input[ty - 1].charAt(tx + 1);
			// down-right
			const db1 = input[ty + 1].charAt(tx + 1);
			// down-left
			const da2 = input[ty + 1].charAt(tx - 1);
			// up-left
			const db2 = input[ty - 1].charAt(tx - 1);
			if (
				((da1 === "M" && da2 === "S") || (da1 === "S" && da2 === "M")) &&
				((db1 === "M" && db2 === "S") || (db1 === "S" && db2 === "M"))
			) {
				return 1;
			}
		} else {
			return 0;
		}

		return 0;
	};

	for (let y = 0; y < input.length; y++) {
		const row = input[y];
		for (let x = 0; x < row.length; x++) {
			const c = row.charAt(x);
			if (c === "A") {
				total += testFromA(x, y);
			}
		}
	}

	return total;
};

