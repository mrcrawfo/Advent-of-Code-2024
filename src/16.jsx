const test1 = (input) => {
	const { grid, start, end } = input;
	const xMax = grid[0].length;
	const yMax = grid.length;
	const { x: startX, y: startY, direction: startDirection } = start;
	const { x: endX, y: endY } = end;

	let pathsInProgress = [
		{
			score: 0,
			steps: 0,
			direction: startDirection,
			path: [`${startX}:${startY}`],
		},
	];

	const pathsCompleted = [];

	while (pathsInProgress.length) {
		const pathsNextStep = [];

		pathsInProgress.forEach((p) => {
			const { score, steps, direction, path } = p;
			const [x, y] = path[path.length - 1].split(":").map((n) => parseInt(n));
			const uniquePath = new Set([...path]);

			// up
			const stepUp = y > 0 ? grid[y - 1].charAt(x) : "#";
			if (stepUp === ".") {
				const dx = x;
				const dy = y - 1;
				const dScore = { R: 1001, L: 1001, U: 1, D: 2001 }[direction];
				const dSteps = { R: 2, L: 2, U: 1, D: 3 }[direction];
				if (!uniquePath.has(`${dx}:${dy}`)) {
					const nextPath = {
						score: score + dScore,
						steps: steps + dSteps,
						direction: "U",
						path: [...path, `${dx}:${dy}`],
					};
					if (dx === endX && dy === endY) {
						pathsCompleted.push(nextPath);
					} else {
						pathsNextStep.push(nextPath);
					}
				}
			}
			// right
			const stepRight = x < xMax ? grid[y].charAt(x + 1) : "#";
			if (stepRight === ".") {
				const dx = x + 1;
				const dy = y;
				const dScore = { U: 1001, D: 1001, R: 1, L: 2001 }[direction];
				const dSteps = { U: 2, D: 2, R: 1, L: 3 }[direction];
				if (!uniquePath.has(`${dx}:${dy}`)) {
					const nextPath = {
						score: score + dScore,
						steps: steps + dSteps,
						direction: "R",
						path: [...path, `${dx}:${dy}`],
					};
					if (dx === endX && dy === endY) {
						pathsCompleted.push(nextPath);
					} else {
						pathsNextStep.push(nextPath);
					}
				}
			}
			// down
			const stepDown = y < yMax ? grid[y + 1].charAt(x) : "#";
			if (stepDown === ".") {
				const dx = x;
				const dy = y + 1;
				const dScore = { R: 1001, L: 1001, D: 1, U: 2001 }[direction];
				const dSteps = { R: 2, L: 2, D: 1, U: 3 }[direction];
				if (!uniquePath.has(`${dx}:${dy}`)) {
					const nextPath = {
						score: score + dScore,
						steps: steps + dSteps,
						direction: "D",
						path: [...path, `${dx}:${dy}`],
					};
					if (dx === endX && dy === endY) {
						pathsCompleted.push(nextPath);
					} else {
						pathsNextStep.push(nextPath);
					}
				}
			}
			// left
			const stepLeft = x > 0 ? grid[y].charAt(x - 1) : "#";
			if (stepLeft === ".") {
				const dx = x - 1;
				const dy = y;
				const dScore = { U: 1001, D: 1001, L: 1, R: 2001 }[direction];
				const dSteps = { U: 2, D: 2, L: 1, R: 3 }[direction];
				if (!uniquePath.has(`${dx}:${dy}`)) {
					const nextPath = {
						score: score + dScore,
						steps: steps + dSteps,
						direction: "L",
						path: [...path, `${dx}:${dy}`],
					};
					if (dx === endX && dy === endY) {
						pathsCompleted.push(nextPath);
					} else {
						pathsNextStep.push(nextPath);
					}
				}
			}
		});

		// reduce pathsInProgress - iterate through but don't list if another path reaches the same position with a lower score
		const reducedPaths = [];
		const pathEnds = new Set();
		for (let e = 0; e < pathsNextStep.length; e++) {
			const path = pathsNextStep[e].path;
			pathEnds.add(path[path.length - 1]);
		}
		const pathEndsArray = [...pathEnds];
		for (let d = 0; d < pathEndsArray.length; d++) {
			const pathEnd = pathEndsArray[d];
			const paths = pathsNextStep.filter(
				(p) => p.path[p.path.length - 1] === pathEnd,
			);
			paths.sort((a, b) => {
				if (a.score < b.score) {
					return -1;
				} else if (a.score > b.score) {
					return 1;
				}
				return 0;
			});
			reducedPaths.push(paths[0]);
		}

		// console.log(pathsInProgress.length, ' :: ', reducedPaths.length);

		pathsInProgress = [...reducedPaths];
	}

	let minScore;
	for (let i = 0; i < pathsCompleted.length; i++) {
		const score = pathsCompleted[i].score;
		if (!minScore) {
			minScore = score;
		} else if (score < minScore) {
			minScore = score;
		}
	}

	return minScore;
};

